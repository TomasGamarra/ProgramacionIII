package Tp2.Ej9;

public class Objeto {
	int sum=0;
	int dif;
	
	public Objeto (int sum, int dif) {
		this.sum=sum;
		this.dif=dif;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getDif() {
		return dif;
	}
	public void setDif(int dif) {
		this.dif = dif;
	}
}
